-- ------------------------------------------------------------------
-- initdb: очищает таблицы и наполняет их через CRUD-процедуры из п.2
-- (не менее 5 станций и 5 клиентов).
-- ------------------------------------------------------------------
CREATE OR REPLACE PROCEDURE initdb()
LANGUAGE plpgsql
AS $$
BEGIN
    RAISE INFO '=== initdb: очистка базы данных ===';
    -- TRUNCATE с RESTART IDENTITY сбрасывает счётчики id, CASCADE — чистит bookings.
    TRUNCATE TABLE bookings, clients, stations RESTART IDENTITY CASCADE;

    RAISE INFO '=== initdb: добавление станций ===';
    CALL add_station('Москва-Товарная', 100, 12000);
    CALL add_station('Санкт-Петербург-Сортировочный', 80, 15000);
    CALL add_station('Екатеринбург-Грузовой', 60, 9000);
    CALL add_station('Новосибирск-Восточный', 50, 8000);
    CALL add_station('Владивосток-Порт', 40, 20000);
    CALL add_station('Краснодар-Южный', 70, 10000);

    RAISE INFO '=== initdb: добавление клиентов ===';
    CALL add_client('ООО "ТрансЛогистик"',   5000000);
    CALL add_client('АО "МеталлТорг"',        3000000);
    CALL add_client('ООО "АгроЭкспорт"',      2000000);
    CALL add_client('ПАО "НефтеХим"',         8000000);
    CALL add_client('ООО "СтройРесурс"',      1500000);
    CALL add_client('ИП Сидоров',              500000);

    RAISE INFO '=== initdb: завершено ===';
END;
$$;

-- ------------------------------------------------------------------
-- makejob: имитация рабочих операций (не менее 15 вызовов п.4).
-- Демонстрирует как успешные операции, так и срабатывание ограничений.
-- ------------------------------------------------------------------
CREATE OR REPLACE PROCEDURE makejob()
LANGUAGE plpgsql
AS $$
DECLARE
    d0 DATE := CURRENT_DATE - 4;   -- начало периода имитации
    d1 DATE := CURRENT_DATE - 3;
    d2 DATE := CURRENT_DATE - 2;
    d3 DATE := CURRENT_DATE - 1;
    d4 DATE := CURRENT_DATE;
BEGIN
    RAISE INFO '=== makejob: имитация рабочих операций ===';

    -- День 0
    CALL book_capacity(1, 1, 30, d0);   -- Москва / ТрансЛогистик
    CALL book_capacity(1, 2, 40, d0);   -- Москва / МеталлТорг
    CALL book_capacity(2, 4, 50, d0);   -- СПб / НефтеХим

    -- День 1
    CALL book_capacity(1, 3, 35, d1);   -- Москва / АгроЭкспорт
    CALL book_capacity(3, 5, 20, d1);   -- Екатеринбург / СтройРесурс
    CALL book_capacity(5, 4, 40, d1);   -- Владивосток / НефтеХим (на весь лимит)
    CALL book_capacity(5, 1, 5,  d1);   -- Владивосток: лимит исчерпан -> отмена

    -- День 2
    CALL top_up_balance(6, 2000000);    -- пополняем ИП Сидоров
    CALL book_capacity(6, 6, 25, d2);   -- Краснодар / ИП Сидоров
    CALL book_capacity(4, 1, 50, d2);   -- Новосибирск / ТрансЛогистик (весь лимит)
    CALL book_capacity(4, 2, 10, d2);   -- Новосибирск: превышение лимита -> отмена

    -- День 3
    CALL book_capacity(2, 2, 30, d3);   -- СПб / МеталлТорг
    CALL book_capacity(3, 3, 40, d3);   -- Екатеринбург / АгроЭкспорт
    CALL book_capacity(1, 5, 1000, d3); -- Москва / СтройРесурс: не хватит средств/лимита -> отмена

    -- День 4 (сегодня)
    CALL book_capacity(6, 1, 60, d4);   -- Краснодар / ТрансЛогистик
    CALL book_capacity(2, 4, 25, d4);   -- СПб / НефтеХим
    CALL top_up_balance(5, 1000000);    -- пополняем СтройРесурс
    CALL book_capacity(1, 5, 20, d4);   -- Москва / СтройРесурс (теперь средств хватает)
    CALL book_capacity(3, 6, 15, d4);   -- Екатеринбург / ИП Сидоров

    RAISE INFO '=== makejob: завершено ===';
END;
$$;
