"""Кастомные исключения пайплайна.

Свои типы исключений делают обработку ошибок осмысленной: вызывающий код
может отличить «не нашли файл данных» от «не обучена модель» и реагировать
по-разному.
"""


class PipelineError(Exception):
    """Базовое исключение для всех ошибок пайплайна."""


class DataLoadError(PipelineError):
    """Не удалось прочитать или провалидировать исходные данные."""


class DataValidationError(PipelineError):
    """Данные прочитаны, но не прошли проверку схемы/качества."""


class ModelNotTrainedError(PipelineError):
    """Попытка использовать модель до её обучения или загрузки."""


class UnknownModelError(PipelineError):
    """Запрошена модель, которую фабрика не умеет создавать."""
